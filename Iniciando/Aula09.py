mensagem = '   eu adoro comida caseira'

print(mensagem.lower()) #imprime o texto em letras minusculas
print(mensagem.upper()) #imprime o texto em letras maiusculas
print(mensagem.capitalize()) #imprime o texto e deixa a primeira letra em maiusculo
print(mensagem.find('adoro')) #Procura por uma letra ou palavra desejada e informa a posição do index que inicia -1 significa que não encontrou
print(mensagem.replace('caseira', 'feita em casa')) #Encontra e substitui a palavra desejada
print(mensagem.strip()) #removes os espaços antes da primeira letra




